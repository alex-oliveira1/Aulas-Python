# 4. Construa um programa que leia 2 números reais que seja informados pelo usuário. Esse programa deve realizar soma e a multiplicação desses valores e exibir o resultado de ambos.
numero1 = float(input("Digite o primeiro número real: "))
numero2 = float(input("Digite o segundo número real: "))

soma = numero1 + numero2
multiplicacao = numero1 * numero2

print("A soma dos números é:", soma)
print("A multiplicação dos números é:", multiplicacao)
